# g2p.py のモジュールプライベート関数 (発音変換の実経路)
_to_pronunciation = getattr(g2p_mod, "__to_pronunciation")


def _g2p_pron(text: str) -> str:
    """G2P 経路 (normalize → morph → pronounce)"""
    return _to_pronunciation(normalize_text(text))


def _params() -> list:
    # generator를 parametrize에 직접 넘기는 방식은 pytest 10에서 제거 예정이라 리스트로 반환
    return [
        pytest.param(
            cat,
            orig,
            prons,
            id=f"{cat}-{orig}",
            marks=[pytest.mark.xfail(reason=xfail, strict=False)] if xfail else [],
        )
        for cat, orig, prons, xfail in GOLDSET
    ]


class TestGoldset:
    @pytest.mark.parametrize("cat, orig, prons", _params())
    def test_pronunciation(self, cat: str, orig: str, prons: tuple[str, ...]):
        assert _g2p_pron(orig) in prons


class TestFuzzInvariants:
    """ランダム入力に対する g2p() の不変式 (シード固定で決定的)"""

    def test_invariants(self):
        random.seed(42)
        valid = set(SYMBOLS)
        final_set = set(KO_FINALS)
        for _ in range(500):
            n = random.randint(1, 30)
            chars = []
            for _ in range(n):
                r = random.random()
                if r < 0.75:
                    chars.append(chr(random.randint(0xAC00, 0xD7A3)))
                elif r < 0.85:
                    chars.append(" ")
                else:
                    chars.append(random.choice(PUNCTUATIONS + ["…", "~", "3", "A", "ㅋ", "😊", "漢"]))
            raw = "".join(chars)
            norm = normalize_text(raw)
            phones, tones, word2ph = g2p(norm)
            assert len(word2ph) == len(norm) + 2, f"word2ph 길이: {raw!r}"
            assert sum(word2ph) == len(phones), f"word2ph 합: {raw!r}"
            assert all(p in valid for p in phones), f"미등록 심볼: {raw!r}"
            assert all(t == 0 for t in tones), f"톤 0 아님: {raw!r}"
            assert "UNK" not in phones, f"UNK 발생: {raw!r}"
            # 標準発音法適用後の終声は 7 종성のみ
            for p in phones:
                if len(p) == 1 and 0x11A8 <= ord(p) <= 0x11C2:
                    assert p in final_set, f"7종성 밖의 종성 {p!r}: {raw!r}"


