    stats: dict[tuple[str, bool], list[int]] = {}
    fails = []
    for cat, orig, prons, xfail in GOLDSET:
        try:
            got = fn(orig)
        except Exception as e:  # noqa: BLE001 - リポートでは失敗として集計
            got = f"<ERROR {e}>"
        ok = got in prons
        s = stats.setdefault((cat, xfail is not None), [0, 0])
        s[0] += ok
        s[1] += 1
        if not ok and xfail is None:
            fails.append((cat, orig, prons[0], got))
    print(f"\n===== {name} =====")
    total_ok = total_n = xf_ok = xf_n = 0
    for (cat, is_xf), (ok, n) in stats.items():
        print(f"  {cat}{' (어휘 예외)' if is_xf else ''}: {ok}/{n}")
        if is_xf:
            xf_ok += ok
            xf_n += n
        else:
            total_ok += ok
            total_n += n
    print(f"  ---- 규칙 범위 정확도: {total_ok}/{total_n} ({100 * total_ok / total_n:.1f}%)")
    print(f"  ---- 어휘 예외(참고): {xf_ok}/{xf_n}")
    for cat, orig, exp, got in fails:
        print(f"    [{cat}] {orig!r}: expected {exp!r}, got {got!r}")


